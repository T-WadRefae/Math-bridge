/* أدوات مشتركة لعرض الكسور والأرقام العربية (يستخدمها التشخيص والعلاج) */
window.MB = {
  toAr:function(n){ return String(n).replace(/[0-9]/g,function(d){return '٠١٢٣٤٥٦٧٨٩'[d];}); },
  // يحوّل رمز {{بسط/مقام}} إلى كسر مرتّب بالأرقام العربية
  frac:function(txt){
    return String(txt).replace(/\{\{(\d+)\/(\d+)\}\}/g,function(_,n,d){
      var a=window.MB.toAr(n), b=window.MB.toAr(d);
      return '<span class="frac"><span class="num">'+a+'</span><span class="den">'+b+'</span></span>';
    });
  },
  // كل مهارات صف معيّن
  byGrade:function(g){ return window.SKILLS.filter(function(s){return s.grade===g;}); },
  byId:function(id){ return window.SKILLS.filter(function(s){return s.id===id;})[0]; },

  // اختيار اختبار تشخيصي متوازن للصف: يوزّع الأسئلة على العوالم بالتناوب
  selectDiagnostic:function(grade, cap){
    cap = cap || 8;
    var byW = {};
    window.MB.byGrade(grade).forEach(function(s){ (byW[s.world]=byW[s.world]||[]).push(s); });
    var order = Object.keys(byW), out = [], i = 0, total = 0;
    order.forEach(function(w){ total += byW[w].length; });
    while(out.length < Math.min(cap,total) && i < 300){
      var w = order[i % order.length];
      if(byW[w] && byW[w].length) out.push(byW[w].shift());
      i++;
    }
    return out;
  },

  /* ---- حفظ التقدّم محلياً (يغذّي «رحلتي» ولوحة المعلّم لاحقاً) ---- */
  load:function(){
    try { return JSON.parse(localStorage.getItem('mb_state')) || {grade:null,skills:{}}; }
    catch(e){ return {grade:null,skills:{}}; }
  },
  save:function(st){ try{ localStorage.setItem('mb_state', JSON.stringify(st)); }catch(e){} },
  // يسجّل نتيجة تشخيص مهارة: 'ok' أتقنها في الفحص، 'gap' فجوة
  recordDiag:function(grade, skillId, status){
    var st = window.MB.load(); st.grade = grade;
    st.skills[skillId] = st.skills[skillId] || {};
    st.skills[skillId].diag = status;
    st.skills[skillId].t = Date.now();
    window.MB.save(st);
  },
  // يسجّل إتقان مهارة بعد مهمة العلاج
  recordMastery:function(skillId, stars){
    var st = window.MB.load();
    st.skills[skillId] = st.skills[skillId] || {};
    st.skills[skillId].mastered = true;
    st.skills[skillId].stars = stars || 3;
    st.skills[skillId].t = Date.now();
    window.MB.save(st);
  },
};
